#include <stdio.h>

int main()
{
    int resultado;

    // soma
    resultado = 10 + 5; 
    printf("%d\n", resultado);

    // subtração
    resultado = 10 - 5;
    printf("%d\n", resultado);

    // multiplicação
    resultado = 10 * 5;
    printf("%d\n", resultado);

    // divisão
    resultado = 10 / 5;
    printf("%d\n", resultado);

    // resto da divisão
    resultado = 10 % 5;
    printf("%d\n", resultado);
}